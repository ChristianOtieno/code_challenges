# frozen_string_literal: true

# lib/calculator.rb

class Calculator
  def add(ano, bbr, che)
    ano + bbr + che
  end
end

class Calculator
  def subtract(ano, bbr)
    ano - bbr
  end
end

class Calculator
  def multiply(ano, bbr)
    ano * bbr
  end
end

class Calculator
  def divide(ano, bbr)
    ano / bbr
  end
end
